data_summary <- function(df=df){
  cat(paste0("===dataset summary===","\n"))
  cat(paste0("NROW: ",nrow(df),"\n"))
  
  v <- sort(unique(df$measure_id))
  cat(paste0("MEASURE: ",paste(identify_measure(v),collapse=", "),"\n"))
  
  v <- sort(unique(df$metric_id))
  cat(paste0("METRIC: ",paste(identify_metric(v),collapse=", "),"\n"))
  
  v <- sort(unique(df$sex_id))
  cat(paste0("SEX: ",paste(identify_sex(v),collapse=", "),"\n"))

  v <- sort(unique(df$age_id))
  if(is.null(v)){cat(paste0("AGE: NA","\n"))} else
  if(length(v)<=5){cat(paste0("AGE: ",paste(identify_age(v),collapse=", "),"\n"))} else {
    cat(paste0("AGE: ",length(v)," values including: ",paste(identify_age(v[1:5]),collapse=", "),"\n"))}
  
  v <- sort(unique(df$cause_id))
  if(is.null(v)){cat(paste0("CAUSE: NA","\n"))} else
  if(length(v)<=5){cat(paste0("CAUSE: ",paste(identify_cause(v),collapse=", "),"\n"))} else {
    cat(paste0("CAUSE: ",length(v)," values including: ",paste(identify_cause(v[1:5]),collapse=", "),"\n"))}
  
  v <- sort(unique(df$location_id))
  if(is.null(v)){cat(paste0("LOCATION: NA","\n"))} else
  if(length(v)<=5){cat(paste0("LOCATION: ",paste(identify_location(v),collapse=", "),"\n"))} else {
    cat(paste0("LOCATION: ",length(v)," values including: ",paste(identify_location(v[1:5]),collapse=", "),"\n"))}
  
  v <- sort(unique(df$rei_id))
  if(is.null(v)){cat(paste0("REI: NA","\n"))} else
  if(length(v)<=5){cat(paste0("REI: ",paste(identify_rei(v),collapse=", "),"\n"))} else {
    cat(paste0("REI: ",length(v)," values including: ",paste(identify_rei(v[1:5]),collapse=", "),"\n"))}
  
  v <- sort(unique(df$year))
  if(is.null(v)){cat(paste0("YEAR: NA","\n"))} else
