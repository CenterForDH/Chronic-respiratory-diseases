identify_measure <- function(id=NULL){
  list <- c("1"="Deaths","2"="DALYs","3"="YLDs","4"="YLLs","5"="Prevalence","6"="Incidence")
  if(is.null(id)){return (list)} else {
  id <- as.character(id)
  if(any(!id %in% names(list))){stop("invalid id")}
  result <- vector("numeric", length(id))
  for (i in seq_along(id)) {result[i] <- list[[as.character(id[i])]]}
  return(result)}
}

